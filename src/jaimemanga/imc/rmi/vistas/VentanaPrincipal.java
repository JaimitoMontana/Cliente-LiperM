    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        peso = Float.parseFloat(fldWeight.getText());
        altura = Float.parseFloat(fldHigh.getText());
        Thread hilo = new Thread(){
            @Override
            public void run(){
                try{
                    System.out.println("PESO: "+peso);
                    System.out.println("ALTURA: "+altura);
                    DatosImc datos = new DatosImc();
                    datos.setPeso(peso);
                    datos.setAltura(altura);
                    System.out.println("ENVIANDO LOS DATOS  \nESPERANDO RESPUESTAS");
                    datos = remoteCalculateImc.CalcularImc(datos);
                    System.out.println("IMC: "+datos.getResultado() + "\nMensaje: "+datos.getInterpretacion());
                    txtResult.setText(datos.getResultado() + "");
                    txtMessage.setText(datos.getInterpretacion() + "");
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(VentanaPrincipal.this, "Error con el cliente: "+ex.getMessage());
                    System.out.println("Error con el cliente: "+ex.getMessage());
                    ex.printStackTrace();
                }
            }
        };
        hilo.start();
    }//GEN-LAST:event_jButton1ActionPerformed

