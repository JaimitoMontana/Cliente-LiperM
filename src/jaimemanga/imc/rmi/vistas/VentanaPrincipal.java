    private void btnConectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectionActionPerformed
        try {
            if (btnConection.getText().equalsIgnoreCase("Conectar")) {
                
                port = Integer.parseInt(fldPort.getText());
                ip = fldIp.getText();
                summorer = new CallHandler();
                customer = new Client(ip, port, summorer);
                remoteCalculateImc = (IRemotaCalculoImc) customer.getGlobal(IRemotaCalculoImc.class);
                btnConection.setText("Desconectar");
                btnConection.setForeground(Color.red);
                txtStatus.setText("Conectado");
                txtStatus.setForeground(Color.GREEN);

            } else if (btnConection.getText().equalsIgnoreCase("Desconectar")) {
                customer.close();
                btnConection.setText("Conectar");
                btnConection.setForeground(Color.GREEN);
                txtStatus.setText("Desconectado");
                txtStatus.setForeground(Color.red);
            }

        } catch (IOException ex) {
            System.out.println("ERROR AL CONECTAR");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnConectionActionPerformed

